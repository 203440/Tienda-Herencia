public class Pedido {
    int dia;
    int mes;
    int anio;
    public Pedido(){}
    public int  getDia(){
        return dia;
    }
     public int getMes(){
       return mes;
    }
    public int getAnio(){
        return anio;
    }
     public void setDia(int dia){
       this.dia=dia;
    }
     public void setMes(int mes){
       this.mes=mes;
    }
    public void setAnio(int anio){
        this.anio=anio;
    }
}
